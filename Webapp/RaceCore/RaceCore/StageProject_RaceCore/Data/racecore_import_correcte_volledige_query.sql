  ON LOWER(TRIM(c.FirstName)) = LOWER(TRIM(ir.FirstName))
 AND LOWER(TRIM(c.LastName)) = LOWER(TRIM(ir.LastName))
WHERE NOT EXISTS (
    SELECT 1 FROM RaceEntries re
    WHERE re.RaceId = ir.RaceId
      AND re.CyclistId = c.Id
);

